    try std.testing.expectEqual(TerminalType.anode, pa.terminal[4]);
    try std.testing.expectEqual(TerminalType.cathode, pa.terminal[5]);
}

test "computePinOffsets MOSFET with SI dimensions" {
    // Simulate a single NMOS with w=2u, l=0.18u and 4 pins (gate, drain, source, body).
    var pa = try PinEdgeArrays.init(std.testing.allocator, 4);
    defer pa.deinit();

    var da = try DeviceArrays.init(std.testing.allocator, 1);
    defer da.deinit();

    da.types[0] = .nmos;
    da.params[0] = .{ .w = 2e-6, .l = 0.18e-6, .fingers = 1, .mult = 1, .value = 0.0 };

    // All pins belong to device 0.
    pa.device[0] = DeviceIdx.fromInt(0);
    pa.device[1] = DeviceIdx.fromInt(0);
    pa.device[2] = DeviceIdx.fromInt(0);
    pa.device[3] = DeviceIdx.fromInt(0);

    pa.terminal[0] = .gate;
    pa.terminal[1] = .drain;
    pa.terminal[2] = .source;
    pa.terminal[3] = .body;

    pa.computePinOffsets(&da);

    // w=2u -> scaled to 2.0, l=0.18u -> scaled to 0.18
    // Offsets match physical contact positions from gdsii.zig:
    //   gate:   (-0.20, l/2) = (-0.20, 0.09)
    //   drain:  (w/2, l+0.13) = (1.0, 0.31)
    //   source: (w/2, -0.13) = (1.0, -0.13)
    //   body:   (w/2, -0.70) = (1.0, -0.70)

    // gate at (-0.20, l_scaled/2)
    try std.testing.expectApproxEqAbs(@as(f32, -0.20), pa.position[0][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, 0.09), pa.position[0][1], 1e-6);

    // drain at (w/2, l+0.13)
    try std.testing.expectApproxEqAbs(@as(f32, 1.0), pa.position[1][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, 0.31), pa.position[1][1], 1e-6);

    // source at (w/2, -0.13)
    try std.testing.expectApproxEqAbs(@as(f32, 1.0), pa.position[2][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, -0.13), pa.position[2][1], 1e-6);

    // body at (0, -0.70) — x_tap = x (left edge of device)
    try std.testing.expectApproxEqAbs(@as(f32, 0.0), pa.position[3][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, -0.70), pa.position[3][1], 1e-6);
}

test "computePinOffsets resistor two-terminal" {
    var pa = try PinEdgeArrays.init(std.testing.allocator, 2);
    defer pa.deinit();

    var da = try DeviceArrays.init(std.testing.allocator, 1);
    defer da.deinit();

    da.types[0] = .res;
    da.params[0] = .{ .w = 0.0, .l = 0.0, .fingers = 1, .mult = 1, .value = 10e3 };

    pa.device[0] = DeviceIdx.fromInt(0);
    pa.device[1] = DeviceIdx.fromInt(0);
    pa.terminal[0] = .anode;
    pa.terminal[1] = .cathode;

    pa.computePinOffsets(&da);

    // w=0, l=0 -> falls back to default_dim=1.0, half=0.5
    try std.testing.expectApproxEqAbs(@as(f32, -0.5), pa.position[0][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, 0.5), pa.position[1][0], 1e-6);
}

test "computePinOffsets zero-dimensional MOSFET uses default" {
    var pa = try PinEdgeArrays.init(std.testing.allocator, 2);
    defer pa.deinit();

    var da = try DeviceArrays.init(std.testing.allocator, 1);
    defer da.deinit();

    da.types[0] = .pmos;
    // w=0, l=0 -> defaults kick in

    pa.device[0] = DeviceIdx.fromInt(0);
    pa.device[1] = DeviceIdx.fromInt(0);
    pa.terminal[0] = .source;
    pa.terminal[1] = .drain;

    pa.computePinOffsets(&da);

    // default_dim=1.0, w_scaled=1.0, l_scaled=1.0
    // source at (w/2, -0.13) = (0.5, -0.13)
    // drain at (w/2, l+0.13) = (0.5, 1.13)
    try std.testing.expectApproxEqAbs(@as(f32, 0.5), pa.position[0][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, -0.13), pa.position[0][1], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, 0.5), pa.position[1][0], 1e-6);
    try std.testing.expectApproxEqAbs(@as(f32, 1.13), pa.position[1][1], 1e-6);
    // Source and drain should be at distinct positions.
    try std.testing.expect(pa.position[0][1] != pa.position[1][1]);
}
